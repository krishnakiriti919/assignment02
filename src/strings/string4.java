package strings;
public class string4 {
    // ContainsExample
    public static void main(String args[]){
        String name="what do you know about me";
        System.out.println(name.contains("do you know"));//prints true
        System.out.println(name.contains("about"));//prints true
        System.out.println(name.contains("hello"));//prints false
    }
}



